struct luc{
    int val, ile;
};

int main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    unordered_map<int, int> v;
    int a, b;
    cin >> a;
    int max_val = 0;
    for(int i = 0; i < 999; ++i){
        cin >> b;
        v[abs(a - b)]++;
        max_val = max(max_val, v[abs(a - b)]);
        a = b;
    }
    vector<luc> result;
    for(auto x: v){
        result.push_back({x.first, x.second});
    }
    sort(result.begin(), result.end(), [](const luc& a, const luc& b){
        if(a.ile == b.ile){
            return a.val > b.val;
        }
        return a.ile > b.ile;
    });
    cout << max_val << endl;
    for(auto x: result){
        if(max_val == x.ile){
            cout << x.val << "\n";
        }
    }

    return 0;
}