deque<int> q;

vector<LL> inpt;

int main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    int n, m;
    LL a;
    cin >> n >> m;
    REP(i, n){
        cin >> a;
        inpt.push_back(a);
    }

    LL h;
    REP(i, m){
        cin >> h;
        q.clear();
        int counter = 0;
        LL carry = 0; 
        REP(j, n){
            if(inpt[j] > h){
                counter++;
                carry = inpt[j] - h;
                while(!q.empty() && carry > 0){
                    if(q.back() + carry >= h){
                        carry = carry + q.back() - h;
                        q.pop_back();
                        counter++;
                    }
                    else break;
                }
            } else if(inpt[j] < h) {
                q.push_back(inpt[j]);
            } else {
                counter++;
                carry = 0;
            }

        }
        cout << counter << " ";
    }


    return 0;
}